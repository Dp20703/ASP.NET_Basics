using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace MyArithClsLib
{
    public class SimpleArith
    {
        public int sum(int num1, int num2)
        {
            return (num1 + num2);
        }
        public int subtract(int num1, int num2)
        {
            return (num1 - num2);
        }
    }
}
